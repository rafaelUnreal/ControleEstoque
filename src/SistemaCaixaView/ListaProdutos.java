    public void showAllProdutos(List<Produto> prod){
        
        DefaultListModel<String> model = new DefaultListModel<>();
        for(int i=0;i<prod.size();i++){
            System.out.println("Produto View : "+ prod.get(i).getNome());
            model.addElement(prod.get(i).getNome());
            
        }
     
        //TODO PEGAR ITENS DO DATABASE
        this.jList1.setModel(model); 
  
    }
